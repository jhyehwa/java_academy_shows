package shoesMart_END;

public class NoticeDTO {
	private String noticeNum, noticeMemId, noticePwd, noticePosts, noticeDate, noticeTitle;

	public String getNoticeTitle() {
		return noticeTitle;
	}

	public void setNoticeTitle(String noticeTitle) {
		this.noticeTitle = noticeTitle;
	}

	public String getNoticeNum() {
		return noticeNum;
	}

	public void setNoticeNum(String noticeNum) {
		this.noticeNum = noticeNum;
	}

	public String getNoticeMemId() {
		return noticeMemId;
	}

	public void setNoticeMemId(String noticeMemId) {
		this.noticeMemId = noticeMemId;
	}

	public String getNoticePwd() {
		return noticePwd;
	}

	public void setNoticePwd(String noticePwd) {
		this.noticePwd = noticePwd;
	}

	public String getNoticePosts() {
		return noticePosts;
	}

	public void setNoticePosts(String noticePosts) {
		this.noticePosts = noticePosts;
	}

	public String getNoticeDate() {
		return noticeDate;
	}

	public void setNoticeDate(String noticeDate) {
		this.noticeDate = noticeDate;
	}

}
